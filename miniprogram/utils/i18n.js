function normalizeLocale(locale) {
  return DICTIONARY[locale] ? locale : DEFAULT_LOCALE;
}

function isSupportedLocale(locale) {
  return normalizeLocale(locale) === locale;
}

function getLocale() {
  if (typeof wx === "undefined" || !wx.getStorageSync) {
    return DEFAULT_LOCALE;
  }

  try {
    return normalizeLocale(wx.getStorageSync("locale"));
  } catch (error) {
    return DEFAULT_LOCALE;
  }
}

function setLocale(locale) {
  const nextLocale = normalizeLocale(locale);

  if (typeof wx !== "undefined" && wx.setStorageSync) {
    wx.setStorageSync("locale", nextLocale);
  }

  return nextLocale;
}

function getTexts(scope, locale = getLocale()) {
  const normalized = normalizeLocale(locale);
  return DICTIONARY[normalized][scope] || DICTIONARY[DEFAULT_LOCALE][scope];
}

function getTabBarItems(locale = getLocale()) {
  const tabbar = getTexts("tabbar", locale);
  return TABBAR_ROUTES.map((item) => Object.assign({}, item, {
    text: tabbar[item.key],
  }));
}


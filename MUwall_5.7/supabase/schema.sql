create table if not exists public.profiles (
    id uuid primary key references auth.users(id) on delete cascade,
    email text,
    username text not null,
    identity text not null default 'student' check (identity in ('student', 'teacher')),
    avatar text not null default 'icon/default.png',
    major text not null default '',
    bio text not null default '',
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists public.posts (
    id bigint generated by default as identity primary key,
    user_id uuid not null references auth.users(id) on delete cascade,
    title text not null,
    content text not null,
    category text not null check (category in ('campus-share', 'study-help', 'daily-life', 'campus-news', 'qna')),
    author text not null,
    author_avatar text not null default 'icon/default.png',
    author_identity text not null default 'student',
    author_major text not null default '',
    author_bio text not null default '',
    like_count integer not null default 0,
    comment_count integer not null default 0,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists public.post_likes (
    post_id bigint not null references public.posts(id) on delete cascade,
    user_id uuid not null references auth.users(id) on delete cascade,
    created_at timestamptz not null default now(),
    primary key (post_id, user_id)
);

create table if not exists public.comments (
    id bigint generated by default as identity primary key,
    post_id bigint not null references public.posts(id) on delete cascade,
    user_id uuid not null references auth.users(id) on delete cascade,
    content text not null check (char_length(btrim(content)) > 0),
    author text not null,
    author_avatar text not null default 'icon/default.png',
    author_identity text not null default 'student',
    author_major text not null default '',
    author_bio text not null default '',
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

-- Older drafts sometimes created posts.user_id as bigint. Supabase Auth user ids
-- are uuid, so drop the incompatible column before adding the correct one.
do $$
begin
    if exists (
        select 1
        from information_schema.columns
        where table_schema = 'public'
          and table_name = 'posts'
          and column_name = 'user_id'
          and udt_name <> 'uuid'
    ) then
        alter table public.posts drop column user_id cascade;
    end if;
end;
$$;


create or replace function public.refresh_post_counts(target_post_id bigint)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
    update public.posts
    set
        like_count = (
            select count(*)::integer
            from public.post_likes
            where post_id = target_post_id
        ),
        comment_count = (
            select count(*)::integer
            from public.comments
            where post_id = target_post_id
        )
    where id = target_post_id;
end;
$$;

create or replace function public.refresh_post_counts_from_interaction()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
    if tg_op in ('DELETE', 'UPDATE') then
        perform public.refresh_post_counts(old.post_id);
    end if;

    if tg_op in ('INSERT', 'UPDATE') then
        perform public.refresh_post_counts(new.post_id);
    end if;

    return coalesce(new, old);
end;
$$;

drop trigger if exists post_likes_refresh_post_counts on public.post_likes;
create trigger post_likes_refresh_post_counts
after insert or delete or update of post_id on public.post_likes
for each row execute function public.refresh_post_counts_from_interaction();

drop trigger if exists comments_refresh_post_counts on public.comments;
create trigger comments_refresh_post_counts
after insert or delete or update of post_id on public.comments
for each row execute function public.refresh_post_counts_from_interaction();

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
    insert into public.profiles (id, email, username, identity, avatar, major, bio)
    values (
        new.id,
        new.email,
        coalesce(new.raw_user_meta_data ->> 'username', split_part(new.email, '@', 1), 'User'),
        case
            when new.raw_user_meta_data ->> 'identity' in ('student', 'teacher')
            then new.raw_user_meta_data ->> 'identity'
            else 'student'
        end,
        coalesce(new.raw_user_meta_data ->> 'avatar', 'icon/default.png'),
        coalesce(new.raw_user_meta_data ->> 'major', ''),
        coalesce(new.raw_user_meta_data ->> 'bio', '')
    )
    on conflict (id) do nothing;

    return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

